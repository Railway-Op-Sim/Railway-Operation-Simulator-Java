package elements;

public class SwitchTrack extends Track {

	public SwitchTrack(ElementType elementType, int xLocation, int yLocation, boolean electrified, String methodOfElectrification) {
		this.elementType = elementType;
		this.xLocation = xLocation;
		this.yLocation = yLocation;
		this.electrified = electrified;
		this.methodOfElectrification = methodOfElectrification;

	}

}
